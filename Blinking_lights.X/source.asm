STATUS equ 03h
RP0 equ 05
RP1 equ 06

PORTB equ 06h
TRISB equ 86h

COUNTER equ 20h

org 00h

    clrw
    BCF STATUS, RP0
    BCF STATUS, RP1
 
    CLRF PORTB

    BSF STATUS, RP0
    MOVLW 0x00 
    MOVWF TRISB

    BCF STATUS, RP0;

START	MOVLW b'00000010'
    MOVWF PORTB
    MOVLW 0xff
    MOVWF COUNTER

LOOP1 DECFSZ COUNTER
    GOTO LOOP1

    CLRF PORTB
    MOVLW b'00000100'
    MOVWF PORTB
      
    MOVLW 0xff
    MOVWF COUNTER
LOOP2 DECFSZ COUNTER
    GOTO LOOP2

    CLRF PORTB
    MOVLW b'00001000'
    MOVWF PORTB
    
    MOVLW 0xff
    MOVWF COUNTER
LOOP3 DECFSZ COUNTER
    GOTO LOOP3
      
    GOTO START
END
	



