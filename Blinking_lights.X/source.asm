STATUS equ 03h
RPZ equ 05
RPO equ 06

PORTB equ 06h
TRISB equ 86h

COUNTER equ 20h

org 00h

    clrw
    BCF STATUS, RPZ
    BCF STATUS, RPO
 
    CLRF PORTB

    BSF STATUS, RPZ
    MOVLW 0x00 
    MOVWF TRISB

    BCF STATUS, RPZ;

START	MOVLW b'00000010'
    MOVWF PORTB
    MOVLW 0xff
    MOVWF COUNTER

LOOP1 DECFSZ COUNTER
    GOTO LOOP1

    CLRF PORTB
    MOVLW b'00000100'
    MOVWF PORTB
      
    MOVLW 0xff
    MOVWF COUNTER
LOOP2 DECFSZ COUNTER
    GOTO LOOP2

    CLRF PORTB
    MOVLW b'00001000'
    MOVWF PORTB
    
    MOVLW 0xff
    MOVWF COUNTER
LOOP3 DECFSZ COUNTER
    GOTO LOOP3
      
    GOTO START
END
	



